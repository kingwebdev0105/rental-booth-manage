			recipient_id: 1,
			type: 'socket',
			token: null,
			message: null
		};

		conn.onopen = function (e) {
			conn.send(JSON.stringify(client));
		};

		conn.onmessage = function (e) {
			var data = JSON.parse(e.data);

			if (data.message) {
				updateBooth(data.message);
			}
		};

		function updateBooth(recvData) {

			var elem = $('[data-id="' + recvData[0] + '"]');

			switch(parseInt(recvData[1])) {
				case 0:
					if(elem.hasClass('room-entranced')) {
						elem.removeClass("room-entranced");
					} else if(elem.hasClass('room-paused')){
						elem.removeClass("room-paused");
					}
					
					showRemainSeats(recvData);
					break;
				case 1:
					elem.addClass("room-entranced");
					showRemainSeats(recvData);
					break;
				case 2:
					elem.addClass("room-paused");
					showRemainSeats(recvData);
					break;
				case 3:

					break;
			}

		}

		function showRemainSeats(data) {
			$('#perabo').text(data[2]);
			$('#perabo_pair').text(data[3]);
			$('#executive').text(data[4]);
			$('#premium').text(data[5]);
		}

		function GoInFullscreen(element) {
			if(element.requestFullscreen)
				element.requestFullscreen();
			else if(element.mozRequestFullScreen)
				element.mozRequestFullScreen();
			else if(element.webkitRequestFullscreen)
				element.webkitRequestFullscreen();
			else if(element.msRequestFullscreen)
				element.msRequestFullscreen();
		}

		function GoOutFullscreen() {
			if(document.exitFullscreen)
				document.exitFullscreen();
			else if(document.mozCancelFullScreen)
				document.mozCancelFullScreen();
			else if(document.webkitExitFullscreen)
				document.webkitExitFullscreen();
			else if(document.msExitFullscreen)
				document.msExitFullscreen();
		}

		function IsFullScreenCurrently() {
			var full_screen_element = document.fullscreenElement || document.webkitFullscreenElement || document.mozFullScreenElement || document.msFullscreenElement || null;
			
			// If no element is in full-screen
			if(full_screen_element === null)
				return false;
			else
				return true;
		}

		$("#full-screen-btn").on('click', function() {

			if(IsFullScreenCurrently()) {
				GoOutFullscreen();
			}
			else {
				GoInFullscreen($('body').get(0));
			}
		});

		$(document).on('fullscreenchange webkitfullscreenchange mozfullscreenchange MSFullscreenChange', function() {
			if(IsFullScreenCurrently()) {
				$("#full-screen-btn span").text('全画面取消');
				$('#toolbar').hide();
			}
			else {
				$("#full-screen-btn span").text('全画面表示');
				$('#toolbar').show();
			}
		});

